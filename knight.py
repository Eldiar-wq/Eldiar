x1 = int(input("Аттың бастапқы X координатасы (1-8): "))
y1 = int(input("Аттың бастапқы Y координатасы (1-8): "))
x2 = int(input("Аттың соңғы X координатасы (1-8): "))
y2 = int(input("Аттың соңғы Y координатасы (1-8): "))

dx = abs(x1 - x2)
dy = abs(y1 - y2)

if (dx == 2 and dy == 1) or (dx == 1 and dy == 2):
    print("Ат бұл орынға жете алады!")
else:
    print("Ат бұл орынға жете алмайды!")
